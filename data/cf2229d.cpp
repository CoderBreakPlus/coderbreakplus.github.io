inline ll qpow(ll a,ll b){
	ll ans=1, base=a;
	while(b){
		if(b&1) ans=ans*base%mod;
		base=base*base%mod; b>>=1;
	}
	return ans;
}
inline ll INV(ll x){ return qpow(x, mod-2); }

int n,a[100005],b[100005];

bool check(int mid){
	int prv=-1,c0=0,c2=0;
	for(int i=1;i<=n;i++){
		int x=(a[i]>=mid)+(b[i]>=mid);
		if(x==1)continue;
		if(x==2)c2++,prv=2;
		else if(x==0&&prv!=0)c0++,prv=0;
	}
	return c2>c0;
}
void procedure(){
	n=read();
	for(int i=1;i<=n;i++)a[i]=read();
	for(int i=1;i<=n;i++)b[i]=read();
	int L=1,R=2*n,ans=1;
	while(L<=R){
		int mid=(L+R)>>1;
		if(check(mid)){
			ans=mid;
			L=mid+1;
		}else R=mid-1;
	}
	printf("%d\n",ans);
}
